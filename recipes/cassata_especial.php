              <h4>Modo de Fazer a Cassata</h4>
              <ul>
                <li>Bater no liquidificador as 4 gemas com o leite condensado, o leite e a Maizena</li>
                <li>Levar ao fogo baixo, mexendo sempre até ferver e engrossar</li>
                <li>Deixar esfriar</li>
                <li>À parte, bater as claras em neve firme</li>
                <li>Aos poucos ir colocando 8 C de sopa de açúcar</li>  
                <li>Bater até formar um merengue</li>   
                <li>Misturar os 2 cremes, sem bater: creme + merengue</li>
                <li>Colocar na forma de pudim já caramelizada com a calda</li> 
                <li>Levar ao freezer tampado: tampa da forma ou papel alumínio</li>
                <li>Na hora de servir, colocar no fogo para soltar</li>           
             </ul>  

             <h4>Modo de Fazer a Calda de Açúcar Queimado</h4>
              <ul>
                <li>Misturar a água e o açúcar</li>
                <li>Levar ao microondas em recipiente de vidro refratário</li>
                <li>Deixar na potência 100% por aproximadamente 1min15s</li>
                <li>Retirar do microondas e verificar se está no ponto</li>                
