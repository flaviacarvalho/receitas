              <h4>Modo de Fazer o Bolo</h4>
              <ul>
                <li>Bater na batedeira a manteiga com 1 xícara de açúcar durante 3 min (reserve a outra xícara de açúcar para o caramelo)</li>
                <li>Acrescentar os ovos e bater mais um pouco (até ficar homogêneo): durante 3 min</li>
                <li>Adicionar o óleo e bater mais um pouco: durante 3 min</li>
                <li>Acrescentar a farinha e o leite: bater por mais 3 min</li>
                <li>À parte, derreta 1 xícara de açúcar (reservada) até o ponto de caramelo</li>
                <li>Juntar o caramelo à massa do bolo, misturando bem para que o caramelo não endureça</li>
                <li>Acrescentar o Royal e bater apenas até misturar (não pode bater muito)</li>
                <li>Forno 200 graus durante +ou- 30 min (grade de cima)</li>
                <li>Observação: Se quiser incrementar, adicione castanhas picadas na massa.</li>
             </ul>     
              
              <h4>Modo de Fazer a Cobertura</h4>
              <ul>
                <li>Misturar todos ingredientes da cobertura</li>
                <li>Levar ao fogo baixo e deixar ferver até engrossar</li>
                <li>Mexer de vez em quando: durante uns 15 min</li> 
             </ul>        
            
