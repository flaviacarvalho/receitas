                <li>1 xícara de suco de laranja</li>  
                <li>2 xícaras de farinha de trigo</li>              
                <li>2 c de chá de Royal </li>
              </ul>
              
              <h4>Ingredientes da Cobertura de Banana Caramelada</h4> 
              <ul>
                <li>1 xícara de açúcar</li>
                <li>¼ xícara de água</li>
                <li>½ c de chá de suco de limão</li>
                <li>2 bananas: picar e ferver junto com o açúcar</li>                
              </ul>
            </section>
          </div>
          
          <div class="col-sm-6">
            <section class="modo-fazer">
              <h4>Modo de Fazer a Cobertura: fazer antes do bolo!</h4>
              <ul>
                <li>Para o caramelo: colocar em uma panela o açúcar, a água e o suco de limão</li>
                <li>Levar ao fogo baixo, com a penela tampada e ferver por 3 min</li>
                <li>Retirar a tampa, colocar as bananas picadas e deixar a calda ficar cozinhando até ficar dourada</li>
                <li>Girar regularmente a panela para não queimar</li>
                <li>Quando o caramelo estiver pronto, despejar em uma forma redonda de vidro para bolo de 30cm (sem furo no meio)</li>
              </ul>

              <h4>Modo de Fazer o Bolo</h4>
              <ul>
                <li>Bater na batedeira os 4 primeiros ingredientes durante 5 min</li>
                <li>Acrescentar a farinha de trigo e bater por mais 5 min</li>
                <li>Acrescentar o Royal e bater apenas até misturar (não pode bater muito)</li>
                <li>Despejar a massa na forma sobre as bananas carameladas</li>
                <li>Forno 200 graus durante +ou- 30 min (grade de cima)</li>
                <li>Vire o bolo ainda quente em um prato (desenforme)</li>
                <li>Observação1: Fiz com 1 banana só, em rodelas, queimou um pouco e grudou</li>
                <li>Observação2: Fiz com 2 bananas cortadas ao comprido em 4 (grossinhas) e ficou tri bom!</li>                
              </ul>            
