              <h4>Modo de Fazer o Creme Branco</h4>
              <ul>
                <li>Desmanchar as gemas e passar em uma peneira</li>
                <li>Misturar o leite condensado com o leite e as gemas peneiradas</li>
                <li>Levar ao fogo baixo, mexendo sempre até ferver e engrossar</li>
                <li>Quando estiver pronto, retirar do fogo e misturar a baunilha</li>
                <li>Colocar nos potinhos e levar para a geladeira</li>  
                <li>Observação: Colocando meio a meio nos potinhos, rende 9 porções</li>              
             </ul>  

             <h4>Modo de Fazer o Mousse de Chocolate</h4>
              <ul>
                <li>Derreter o chocolate no microondas: potência 50% de 1min em 1min (colocar 1min, retirar e misturar, +1min... até derreter todo)</li>
                <li>Misturar o creme de leite</li>
                <li>Deixar a mistura de chocolate e creme de leite bem homogênea</li>
                <li>Bater as claras em neve firme</li>
                <li>Misturar delicadamente as claras em neve com a mistura de chocolate</li>
                <li>Colocar nos potinhos, em cima do creme branco e levar para a geladeira</li> 
                <li>Dica1: É interessante cobrir com filme plástico cada potinho</li> 
                <li>Dica2: Colocar em potes menores, pois nem todos conseguem comer um inteiro desses :)</li>              
